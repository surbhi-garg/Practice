package com.ttn.barista;

//This class can further be modified if we give customer chances to customize the order, like an extra topping

public class Product {
    String name;
    float price;

    public Product(String name, float price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
}
